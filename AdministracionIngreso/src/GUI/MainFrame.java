    public boolean validarAdministrador(){
        
        JPanel panel = new JPanel();
        JLabel label = new JLabel("Ingrese Contrase√±a de Administrador");
        JPasswordField pass = new JPasswordField(10);
        panel.add(label);
        panel.add(pass);
        String[] options = new String[]{"OK", "Cancel"};
        int option = JOptionPane.showOptionDialog(null, panel, "Administrador",
                              JOptionPane.NO_OPTION, JOptionPane.PLAIN_MESSAGE,
                              null, options, options[1]);
        pass.setFocusable(true);
        pass.requestFocus();
        if(option == 0) // pressing OK button
        {
            char[] password = pass.getPassword();
            //String password = pass.getText();
            String passadmin = new String(password);
            //System.out.println("Your password is: " + new String(password));
            ConexionBD con = new ConexionBD();
            //con.conectar();

            try {
                ResultSet consulta = con.consultar("Select estado from administrador where pass = SHA('"+passadmin+"');");
                String estado="";
                //con.cerrar();
                while (consulta.next()) {                
                    estado = consulta.getString("estado");
                }
                if ("activo".equals(estado)) {
                    return true;
                }
                //System.out.println(estado);
            } catch (SQLException ex) {
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
            } catch (Throwable ex) {
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        return false;
    }

